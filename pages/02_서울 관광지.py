# -----------------------------
# 지도 생성
# -----------------------------
seoul_center = [37.5665, 126.9780]

m = folium.Map(
    location=seoul_center,
    zoom_start=11,
    tiles="CartoDB Positron"
)

# 마커 추가
for name, info in tourist_spots.items():

    popup_html = f"""
    <b>{name}</b><br>
    {info['desc']}<br>
    🚇 {info['station']}
    """

    folium.Marker(
        location=[info["lat"], info["lon"]],
        popup=popup_html,
        tooltip=name,
        icon=folium.Icon(color="blue", icon="info-sign")
    ).add_to(m)

# -----------------------------
# 지도 출력 (80% 크기)
# -----------------------------
